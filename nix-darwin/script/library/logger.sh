#!/usr/bin/env bash
# logger.sh
#
# 標準化されたログ出力関数を提供する。
# 各スクリプトが echo の体裁を個別に持たないよう、出力を一元化する (DRY)。
#   - info  : 進捗・通知を標準出力へ
#   - error : 失敗を標準エラー出力へ (パイプやリダイレクトと分離できる)
#
# このファイルは実行せず、他スクリプトから source して利用する。

# 進捗・通知メッセージを標準出力へ出力する。
info() {
  printf '[INFO] %s\n' "$*"
}

# エラーメッセージを標準エラー出力へ出力する。
error() {
  printf '[ERROR] %s\n' "$*" >&2
}
